function traffic(n, m, vehicle) {
  let max = 0;
  for (let i = 0; i < n; i++) {
    let count = 0;
    let val = m;
    if (val == 1) {
      count++;
    }
    let j = i;
    while (val != 0 && j < n) {
      count++;
      if (vehicle[j] == 0) {
        val--;
      }
      j++;
    }
    if (count > max) {
      max = count;
    }
  }
  if (vehicle[n - 1] == 1) max += 1;
  return max;
}

